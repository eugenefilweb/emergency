<div style="text-align: center; font-weight: bolder;">REIMBURSEMENT EXPENSE RECEIPT</div>
<div style="text-align: center; font-weight: bolder;">(RER)</div>

<h4 style="text-align: center; font-weight: normal;">INSTRUCTIONS</h4>

<div style="display: flex; gap: 5px; margin-bottom: 30px;">
    <div>A.</div>
    <div>The RER is used to support the expenses which cannot be conveniently
        issued official receipts/invoice. It shall be submitted to the Accounting
        Division/Unit together with the LR and other supporting documents. It shall
        be prepared by fund cluster.
    </div>
</div>

<div style="display: flex; gap: 5px;">
    <div>B.</div>
    <div>
        <div>This form shall be accomplished as follows:</div>
        <div style="display: flex; gap: 15px;">
            <span>1.</span>
            <div><span style="font-weight: bolder;">Entity Name</span> - the name of the agency/entity</div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>2.</span>
            <div><span style="font-weight: bolder;">Date</span> - the date of the receipt</div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>3.</span>
            <div><span style="font-weight: bolder;">Fund Cluster</span> - the fund cluster name/code based on the UACS in
                which the disbursement shall be charged
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>4.</span>
            <div><span style="font-weight: bolder;">RER No.</span> - The number assigned to the RER
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>5.</span>
            <div><span style="font-weight: bolder;">Name</span> - the name of the official/employee who travelled
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>6.</span>
            <div><span style="font-weight: bolder;">Official Designation</span> - the official designation or position of the official/employee
                who travelled
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>7.</span>
            <div><span style="font-weight: bolder;">In Words</span> - the amount in words of the actual payment made by the
                Official/employee who travelled
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>8.</span>
            <div><span style="font-weight: bolder;">In Figures</span> - the amount in figures of the actual payment made by the
                Official/employee who travelled
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span>9.</span>
            <div><span style="font-weight: bolder;">Payments</span> - the payments for subsistence, services, rental or transporatation which show
                inclusive dates, purpose, distance, inclusive points of travel, etc.
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span style="width: 13px;">10.</span>
            <div><span style="font-weight: bolder;">Payee</span> - the name, signature and the address of the payee
            </div>
        </div>
        <div style="display: flex; gap: 15px;">
            <span style="width: 13px;">11.</span>
            <div><span style="font-weight: bolder;">Witness</span> - the name, signature and the address of the witness
                to the actual payment made by the official/employee who travelled
            </div>
        </div>
    </div>
</div>
